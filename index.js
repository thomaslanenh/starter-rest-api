// Create or Update an item

const axios = require("axios")
app.listen(process.env.PORT || 3000)

app.post('/:col/:key', async (req, res) => {
  console.log(req.body)

  const col = req.params.col
  const key = req.params.key
  console.log(`from collection: ${col} delete key: ${key} with params ${JSON.stringify(req.params)}`)
  const item = await db.collection(col).set(key, req.body)
  console.log(JSON.stringify(item, null, 2))
  res.json(item).end()
})

// Delete an item
app.delete('/:col/:key', async (req, res) => {
  const col = req.params.col
  const key = req.params.key
  console.log(`from collection: ${col} delete key: ${key} with params ${JSON.stringify(req.params)}`)
  const item = await db.collection(col).delete(key)
  console.log(JSON.stringify(item, null, 2))
  res.json(item).end()
})

// Get a single item
app.get('/:col/:key', async (req, res) => {
  const col = req.params.col
  const key = req.params.key
  console.log(`from collection: ${col} get key: ${key} with params ${JSON.stringify(req.params)}`)
  const item = await db.collection(col).get(key)
  console.log(JSON.stringify(item, null, 2))
  res.json(item).end()
})

// Get a full listing
app.get('/:col', async (req, res) => {
  const col = req.params.col
  console.log(`list collection: ${col} with params: ${JSON.stringify(req.params)}`)
  const items = await db.collection(col).list()
  console.log(JSON.stringify(items, null, 2))
  res.json(items).end()
})

app.get("/update-quote", async (req,res) => {
  console.log("Just got a request!")
  console.log(req.params)
  // test func

  const updateDeal = async () => {
    let data = JSON.stringify({
      "properties": {
        "lock_in_pricing": req.params.checkbox
      }
    });

    let config = {
      method: 'patch',
      maxBodyLength: Infinity,
      url: `https://api.hubapi.com/crm/v3/objects/deals/${req.params.deal_id}`,
      headers: {
        'authorization': `Bearer ${process.env.HSKEY}`,
        'content-type': 'application/json'
      },
      data: data
    };

    try {
      await axios.request(config);
      res.status(200).send({success: true})
    } catch (e) {
      console.log(e)
      res.status(200).send({success: false, error: e})
    }
  }

  await updateDeal();
})

// Catch all handler for all other request.
app.use('*', (req, res) => {
  res.json({ msg: 'no route handler found' }).end()
})

// Start the server
const port = process.env.PORT || 3000
app.listen(port, () => {
  console.log(`index.js listening on ${port}`)
})
